#!/usr/bin/env node

/**
 * Script para dividir CSS por componentes e criar carregamento condicional
 * Analisa quais componentes usam quais bibliotecas CSS pesadas
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

// Mapear componentes e suas dependências CSS
const componentCSSMap = {
  'carousel': {
    files: ['/css/slick.css', '/css/slick-theme.css'],
    components: ['Cars.jsx', 'Testimonials.jsx', 'RelatedCars.jsx'],
    size: '~25KB',
    description: 'CSS para carousels e sliders'
  },
  'animations': {
    files: ['/css/animate.css', '/css/animations-lazy.css'],
    components: ['Hero.jsx', 'About.jsx', 'Cars.jsx'],
    size: '~15KB',
    description: 'CSS para animações e transições'
  },
  'gallery': {
    files: ['/css/jquery.fancybox.min.css'],
    components: ['Single.jsx', 'Gallery.jsx'],
    size: '~8KB',
    description: 'CSS para galerias de fotos'
  },
  'mobile-menu': {
    files: ['/css/mmenu.css'],
    components: ['Header.jsx', 'MobileMenu.jsx'],
    size: '~12KB',
    description: 'CSS para menu mobile'
  }
};

async function analyzeComponentCSS() {
  console.log('🔍 Analisando divisão de CSS por componentes...\n');

  let totalSize = 0;
  let totalFiles = 0;

  for (const [componentType, config] of Object.entries(componentCSSMap)) {
    console.log(`📦 ${componentType.toUpperCase()}`);
    console.log(`   Arquivos CSS: ${config.files.join(', ')}`);
    console.log(`   Componentes: ${config.components.join(', ')}`);
    console.log(`   Tamanho estimado: ${config.size}`);
    console.log(`   Descrição: ${config.description}\n`);

    // Verificar se os arquivos CSS existem
    for (const cssFile of config.files) {
      const fullPath = path.join(projectRoot, 'public', cssFile);
      if (fs.existsSync(fullPath)) {
        const stats = fs.statSync(fullPath);
        console.log(`   ✅ ${cssFile} existe (${Math.round(stats.size / 1024)}KB)`);
        totalSize += stats.size;
        totalFiles++;
      } else {
        console.log(`   ⚠️  ${cssFile} não encontrado`);
      }
    }
    console.log('');
  }

  console.log(`📊 RESUMO:`);
  console.log(`   Total de arquivos CSS: ${totalFiles}`);
  console.log(`   Tamanho total: ${Math.round(totalSize / 1024)}KB`);
  console.log(`   Economia potencial: ~40-60% com carregamento condicional\n`);

  // Gerar código para carregamento dinâmico
  generateDynamicLoader();
}

function generateDynamicLoader() {
  console.log('🛠️  Gerando código para carregamento dinâmico...\n');

  const loaderCode = `
// 🎯 CSS Component Loader - Generated by css-component-split.js
const cssComponentMap = ${JSON.stringify(componentCSSMap, null, 2)};

class DynamicCSSLoader {
  constructor() {
    this.loadedCSS = new Set();
    this.loadingPromises = new Map();
  }

  async loadComponentCSS(componentType) {
    if (!cssComponentMap[componentType]) {
      console.warn(\`Component type '\${componentType}' not found\`);
      return;
    }

    const config = cssComponentMap[componentType];
    const promises = config.files.map(file => this.loadCSS(file));
    
    try {
      await Promise.allSettled(promises);
      console.log(\`✅ \${componentType} CSS loaded (\${config.size})\`);
    } catch (error) {
      console.warn(\`⚠️ Error loading \${componentType} CSS:\`, error);
    }
  }

  async loadCSS(cssPath) {
    const id = cssPath.replace(/[^a-zA-Z0-9]/g, '-');
    
    if (this.loadedCSS.has(id) || document.getElementById(id)) {
      return Promise.resolve();
    }

    if (this.loadingPromises.has(id)) {
      return this.loadingPromises.get(id);
    }

    const promise = new Promise((resolve, reject) => {
      const link = document.createElement('link');
      link.rel = 'stylesheet';
      link.href = cssPath;
      link.id = id;
      
      link.onload = () => {
        this.loadedCSS.add(id);
        this.loadingPromises.delete(id);
        resolve();
      };
      
      link.onerror = () => {
        this.loadingPromises.delete(id);
        resolve(); // Don't reject - continue without CSS
      };

      document.head.appendChild(link);
    });

    this.loadingPromises.set(id, promise);
    return promise;
  }
}

// Export singleton
export const dynamicCSSLoader = new DynamicCSSLoader();
`;

  const outputPath = path.join(projectRoot, 'src', 'utils', 'dynamicCSSLoader.js');
  fs.writeFileSync(outputPath, loaderCode.trim());
  console.log(`✅ Gerado: ${outputPath}\n`);

  // Gerar exemplo de uso
  const usageExample = `
// 🎯 Exemplo de uso nos componentes:

import { dynamicCSSLoader } from '@/utils/dynamicCSSLoader';

// No componente Cars.jsx
useEffect(() => {
  dynamicCSSLoader.loadComponentCSS('carousel');
}, []);

// No componente Gallery.jsx  
useEffect(() => {
  dynamicCSSLoader.loadComponentCSS('gallery');
}, []);

// No componente Hero.jsx
useEffect(() => {
  dynamicCSSLoader.loadComponentCSS('animations');
}, []);
`;

  console.log('📝 EXEMPLO DE USO:');
  console.log(usageExample);
}

// Executar análise
analyzeComponentCSS().catch(console.error);